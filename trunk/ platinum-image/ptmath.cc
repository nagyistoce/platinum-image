unsigned int get_factorial(unsigned int i)
{
	if(i==1){
		return 1;
	}
	else{
		return i*get_factorial(i-1);
	}
}

unsigned int get_permutations(unsigned int n, unsigned int r)
{
	return get_factorial(n)/(get_factorial(r)*get_factorial(n-r));
}